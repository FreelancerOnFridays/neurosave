_GIFS_DIR = Path(__file__).parent / "gifs"


def _resolve_gif(gif_url: str) -> FSInputFile | str:
    if gif_url.startswith("http"):
        return gif_url
    path = _GIFS_DIR / gif_url
    return FSInputFile(str(path))


