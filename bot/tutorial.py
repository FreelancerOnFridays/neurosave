def build_keyboard_dict(page_idx: int) -> dict[str, object]:
    n = len(PAGES)
    prev_idx = (page_idx - 1) % n
    next_idx = (page_idx + 1) % n
    return {
        "inline_keyboard": [
            [
                {"text": "◀️", "callback_data": f"tut:{prev_idx}"},
                {"text": f"{page_idx + 1} / {n}", "callback_data": "tut:noop"},
                {"text": "▶️", "callback_data": f"tut:{next_idx}"},
            ],
            [{"text": "❓ Частые ошибки и ограничения", "callback_data": "tut:faq"}],
        ]
    }


def build_keyboard_markup(page_idx: int) -> InlineKeyboardMarkup:
    n = len(PAGES)
    prev_idx = (page_idx - 1) % n
    next_idx = (page_idx + 1) % n
    return InlineKeyboardMarkup(
        inline_keyboard=[
            [
                InlineKeyboardButton(text="◀️", callback_data=f"tut:{prev_idx}"),
                InlineKeyboardButton(text=f"{page_idx + 1} / {n}", callback_data="tut:noop"),
                InlineKeyboardButton(text="▶️", callback_data=f"tut:{next_idx}"),
            ],
            [InlineKeyboardButton(text="❓ Частые ошибки и ограничения", callback_data="tut:faq")],
        ]
    )


_MAX_CAPTION = 1024

# Cached Telegram file_ids after first upload — keyed by video filename
_file_id_cache: dict[str, str] = {}


async def send_tutorial_page(bot: Bot, chat_id: int, page_idx: int) -> None:
    page = PAGES[page_idx]
    keyboard = build_keyboard_markup(page_idx)
    if page.gif_url:
        cached = _file_id_cache.get(page.gif_url)
        video: FSInputFile | str = cached if cached else _resolve_gif(page.gif_url)

        loading_msg = await bot.send_message(chat_id=chat_id, text="⏳ Загрузка видео…")
        msg = None
        try:
            if len(page.caption) <= _MAX_CAPTION:
                msg = await bot.send_video(
                    chat_id=chat_id,
                    video=video,
                    caption=page.caption,
                    parse_mode="HTML",
                    reply_markup=keyboard,
                    supports_streaming=True,
                )
            else:
                msg = await bot.send_video(
                    chat_id=chat_id,
                    video=video,
                    supports_streaming=True,
                )
                await bot.send_message(
                    chat_id=chat_id,
                    text=page.caption,
                    parse_mode="HTML",
                    reply_markup=keyboard,
                )
        finally:
            try:
                await loading_msg.delete()
            except Exception:
                pass

        if msg and not cached and msg.video:
            _file_id_cache[page.gif_url] = msg.video.file_id
    else:
        await bot.send_message(
            chat_id=chat_id,
            text=page.caption,
            parse_mode="HTML",
            reply_markup=keyboard,
        )
